document.getElementById("run").addEventListener("click", randomColor);

function randomColor() {
  var num = Math.round(0xffffff * Math.random());
  let r = num >> 16;
  let g = (num >> 8) & 255;
  let b = num & 255;
  let bgColor = "rgb(" + r + ", " + g + ", " + b + ")";
  document.body.style.backgroundColor = bgColor;
}
